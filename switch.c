//Write a switch c program to write the total number of days in a month
#include <stdio.h>
#include <conio.h>
void main()
{
int month;
printf("Write any number of the day of the month");
scanf("%d",&month);
switch(month)
{
case 1:

printf("Monday");
break;

case 2:
printf("tuesday");
break;


case 3:
printf("wednesday");
break;

case 4:
printf("thursday");
break;

case 5:
printf("friday");
break;
}
getch();
}
    
     

    














